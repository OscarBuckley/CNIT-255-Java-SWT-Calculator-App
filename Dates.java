package CNIT255Lab6;

public class Dates {
	
	protected int year, month, day;
	
	public int getyear() {

		return year;

		}

	public void setyear(int newyear) {

		year = newyear;

		}
	
	public int getmonth() {

		return month;

		}

	public void setmonth(int newmonth) {

		month = newmonth;

		}
	
	public int getday() {

		return day;

		}

	public void setday(int newday) {

		day = newday;

		}

}
